
public class Coordniates {
	
	//this class contains 2 coordinates, x and y of the next square the ghost should go to 
	int x; 
	int y; 
	public Coordniates(int gx, int gy)
	{
		x = gx; 
		y = gy; 
	}
	
	public int getX()
	{
		return x;
	}
	public int getY() 
	{
		return y;
	}
}
